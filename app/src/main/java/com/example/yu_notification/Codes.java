package com.example.yu_notification;

public class Codes {
    String key, title, code;

    public String getKey() { return key; }

    public String getTitle() {
        return title;
    }

    public String getCode() {
        return code;
    }

    public Codes(String key, String title, String code) {
        this.key = key;
        this.title = title;
        this.code = code;
    }

}
